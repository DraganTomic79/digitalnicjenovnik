// Current language state
let currentLanguage = "bs";

/**
 * Set the language for the entire page
 * @param {string} lang - Language code ('bs' or 'en')
 */
function setLang(lang) {
  currentLanguage = lang;

  // Update page title
  if (translations[lang] && translations[lang].page_title) {
    document.title = translations[lang].page_title;
  }

  // Update all elements with data-i18n attribute
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang] && translations[lang][key]) {
      el.innerHTML = translations[lang][key];
    }
  });

  // Update all placeholders
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (translations[lang] && translations[lang][key]) {
      el.placeholder = translations[lang][key];
    }
  });

  updateLanguageButtons(lang);
}

/**
 * Update language button styles
 * @param {string} activeLang - Currently active language
 */
function updateLanguageButtons(activeLang) {
  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.classList.remove("btn-primary", "active");
    btn.classList.add("btn-outline-primary");

    const btnLang = btn.getAttribute("data-lang") || btn.textContent.toLowerCase();
    if (btnLang === activeLang) {
      btn.classList.remove("btn-outline-primary");
      btn.classList.add("btn-primary", "active");
    }
  });
}

/**
 * Smooth scroll for anchor links
 */
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
  anchor.addEventListener("click", function(e) {
    e.preventDefault();
    const target = document.querySelector(this.getAttribute("href"));
    if (target) {
      const offsetTop = target.getBoundingClientRect().top + window.scrollY - 80;
      window.scrollTo({
        top: offsetTop,
        behavior: "smooth"
      });
    }
  });
});

/**
 * Contact form submission handler
 */
document.getElementById("contactForm").addEventListener("submit", function(e) {
  e.preventDefault();

  const formData = new FormData(this);
  const name = formData.get("name");
  const email = formData.get("email");
  const subject = formData.get("subject");
  const message = formData.get("message");

  const inputs = this.querySelectorAll("input, textarea");
  let isValid = true;

  // Validate all inputs
  inputs.forEach(input => {
    if (!input.value.trim()) {
      input.classList.add("is-invalid");
      isValid = false;
    } else {
      input.classList.remove("is-invalid");
      input.classList.add("is-valid");
    }
  });

  if (isValid) {
    const mailtoLink = `mailto:dragantmc2@gmail.com?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(`Ime: ${name}\nEmail: ${email}\nPoruka: ${message}`)}`;
    window.location.href = mailtoLink;

    // Reset form
    this.reset();
    inputs.forEach(input => input.classList.remove("is-valid", "is-invalid"));
    alert("Hvala vam! Otvorićemo vašu email aplikaciju za slanje poruke.");
  } else {
    alert("Molimo popunite sva polja pravilno.");
  }
});

/**
 * Package modal form submission
 */
function sendPackageMail() {
  const form = document.getElementById("packageForm");
  const formData = new FormData(form);
  const name = formData.get("name");
  const email = formData.get("email");
  const message = formData.get("message");

  const inputs = form.querySelectorAll("input, textarea");
  let isValid = true;

  // Validate required inputs
  inputs.forEach(input => {
    if (input.hasAttribute("required") && !input.value.trim()) {
      input.classList.add("is-invalid");
      isValid = false;
    } else {
      input.classList.remove("is-invalid");
    }
  });

  if (isValid) {
    const paket = document.getElementById("packageModalLabel").textContent
      .replace("Zatraži ponudu - ", "")
      .replace("Request quote - ", "");

    const subject = encodeURIComponent(`Zainteresovan za ${paket}`);
    const body = encodeURIComponent(`Ime: ${name}\nEmail: ${email}\nPaket: ${paket}\nPoruka: ${message}`);

    window.location.href = `mailto:dragantmc2@gmail.com?subject=${subject}&body=${body}`;

    // Close modal and reset form
    bootstrap.Modal.getInstance(document.getElementById("packageModal")).hide();
    form.reset();
    inputs.forEach(input => input.classList.remove("is-invalid"));
  } else {
    alert("Molimo popunite sva obavezna polja.");
  }
}

/**
 * Observe elements for scroll animations
 */
function observeElements() {
  const elements = document.querySelectorAll(".partner-card, .package-card, .advantage-card");
  const observer = new IntersectionObserver(entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add("animate");
        entry.target.style.opacity = "1";
        entry.target.style.transform = "translateY(0)";
      }
    });
  }, {
    threshold: 0.1
  });

  elements.forEach(el => observer.observe(el));
}

/**
 * Back to top button functionality
 */
const backToTop = document.getElementById("backToTop");
let ticking = false;

function updateScrollElements() {
  backToTop.style.display = window.scrollY > 300 ? "block" : "none";
  ticking = false;
}

window.addEventListener("scroll", () => {
  if (!ticking) {
    requestAnimationFrame(updateScrollElements);
    ticking = true;
  }
});

backToTop.addEventListener("click", () => {
  window.scrollTo({
    top: 0,
    behavior: "smooth"
  });
});

/**
 * DOMContentLoaded - Initialize all functionality
 */
document.addEventListener("DOMContentLoaded", () => {
  // Initialize scroll animations
  observeElements();

  // Language button handlers
  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.addEventListener("click", function(e) {
      e.preventDefault();
      const lang = this.getAttribute("data-lang") || this.textContent.toLowerCase();
      setLang(lang);
    });
  });

  // Set default language
  setLang("bs");

  // Package modal handlers
  const packageModal = document.getElementById("packageModal");
  if (packageModal) {
    packageModal.addEventListener("hide.bs.modal", function() {
      const focusedElement = packageModal.querySelector(":focus");
      if (focusedElement) focusedElement.blur();
    });

    packageModal.addEventListener("hidden.bs.modal", function() {
      document.body.focus();
    });
  }

  // Package button handlers
  document.querySelectorAll(".package-btn").forEach(btn => {
    btn.addEventListener("click", function() {
      const card = this.closest(".card");
      const packageName = card.querySelector(".card-title").textContent;
      const price = card.querySelector(".price-display").textContent;

      document.getElementById("packageModalLabel").textContent = `Zatraži ponudu - ${packageName}`;

      const messageField = document.querySelector('#packageForm textarea[name="message"]');
      if (messageField) {
        messageField.value = `Zainteresovan sam za paket: ${packageName} (${price})`;
      }

      this.blur();
      setTimeout(() => {
        new bootstrap.Modal(document.getElementById("packageModal")).show();
      }, 50);
    });
  });

  // Mobile menu collapse
  const navLinks = document.querySelectorAll(".nav-link");
  const navbarCollapse = document.querySelector(".navbar-collapse");
  navLinks.forEach(link => {
    link.addEventListener("click", () => {
      if (window.innerWidth < 992) {
        new bootstrap.Collapse(navbarCollapse).hide();
      }
    });
  });

  // Zatvori navbar klikom izvan njega
  document.addEventListener("click", (e) => {
    if (window.innerWidth < 992 && navbarCollapse.classList.contains("show")) {
      if (!navbarCollapse.contains(e.target) && !document.querySelector(".navbar-toggler").contains(e.target)) {
        new bootstrap.Collapse(navbarCollapse).hide();
      }
    }
  });

  // Touch device detection
  if ("ontouchstart" in window) {
    document.body.classList.add("touch-device");
  }
});

// Expose functions globally
window.setLang = setLang;
window.sendPackageMail = sendPackageMail;
